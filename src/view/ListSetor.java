    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:

        if(tblSetor.getSelectedRow()<0){
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela");
        }else{
            int linha = tblSetor.getSelectedRow();
            int id = Integer.parseInt((String)  tblSetor.getValueAt(linha, 0));
            DAO.DaoSetor excluirFuncionario = new DAO.DaoSetor();
            excluirFuncionario.excluir(id);

            carregarLista();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterar8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterar8ActionPerformed
        // TODO add your handling code here:
        if(tblSetor.getSelectedRow()<0){
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela");

        }else{
            String codigo, nome, categoria;
            int linha = tblSetor.getSelectedRow();
            codigo = (String) tblSetor.getValueAt(linha,0);
            nome = (String) tblSetor.getValueAt(linha, 1 );
            categoria = (String) tblSetor.getValueAt(linha, 2 );
            
            Alterar_setor alSetor = new Alterar_setor();
            alSetor.resgatar(codigo, nome, categoria);
            alSetor.setVisible(true);
            this.dispose();

        }
    }//GEN-LAST:event_btnAlterar8ActionPerformed

