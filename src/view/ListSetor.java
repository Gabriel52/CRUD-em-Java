    public void carregarLista(){
        DefaultTableModel modelo = (DefaultTableModel) tblSetor.getModel();
        modelo.setNumRows(0);
        DAO.DaoSetor itensSetor = new DAO.DaoSetor();
        ResultSet dados = itensSetor.listar();
        
        try{
            while(dados.next()){
                modelo.addRow(new Object[]{
                    dados.getString(1),
                    dados.getString(2),
                    dados.getString(3)
                
                });
            }
        }catch(SQLException error){
            throw new RuntimeException(error);
        }
    
    }

