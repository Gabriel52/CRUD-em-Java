    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:

        if(tblProduto.getSelectedRow()<0){
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela");
        }else{
            int linha = tblProduto.getSelectedRow();
            int id = Integer.parseInt((String)  tblProduto.getValueAt(linha, 0));
            DAO.DaoProduto excluirFuncionario = new DAO.DaoProduto();
            excluirFuncionario.excluir(id);

            carregarLista();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterar7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterar7ActionPerformed
        // TODO add your handling code here:
        if(tblProduto.getSelectedRow()<0){
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela");

        }else{
            String codigo, nome, validade, preco, quantidade;
            int linha = tblProduto.getSelectedRow();
            codigo = (String) tblProduto.getValueAt(linha,0);
            nome = (String) tblProduto.getValueAt(linha, 1 );
            validade = (String) tblProduto.getValueAt(linha, 2 );
            preco = (String) tblProduto.getValueAt(linha, 3 );
            quantidade = (String) tblProduto.getValueAt(linha, 4 );
            

            Alterar_produtos alProdutos = new Alterar_produtos();
            alProdutos.resgatar(codigo, nome, validade, preco, quantidade);
            alProdutos.setVisible(true);
            this.dispose();

        }
    }//GEN-LAST:event_btnAlterar7ActionPerformed

