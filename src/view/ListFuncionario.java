    public void carregarLista(){
        DefaultTableModel modelo = (DefaultTableModel) tblFuncionario.getModel();
        modelo.setNumRows(0);
        DAO.DaoFuncionario itensFuncionario = new DAO.DaoFuncionario();
        ResultSet dados = itensFuncionario.listar();
        
        try{
            while(dados.next()){
                modelo.addRow(new Object[]{
                    dados.getString(1),
                    dados.getString(2),
                    dados.getString(3),
                    dados.getString(4),
                    dados.getString(5),
                    dados.getString(6),
                    dados.getString(7)
                
                
                });
            };
            
        }catch(SQLException error){
            throw new RuntimeException(error);
        }
    
    }

