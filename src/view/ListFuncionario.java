    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:

        if(tblFuncionario.getSelectedRow()<0){
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela");
        }else{
            int linha = tblFuncionario.getSelectedRow();
            int id = Integer.parseInt((String)  tblFuncionario.getValueAt(linha, 0));
            DAO.DaoFuncionario excluirFuncionario = new DAO.DaoFuncionario();
            excluirFuncionario.excluir(id);

            carregarLista();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterar7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterar7ActionPerformed
        // TODO add your handling code here:
        if(tblFuncionario.getSelectedRow()<0){
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela");

        }else{
            String codigo, nome, rg, cpf, cpe, telefone;
            int linha = tblFuncionario.getSelectedRow();
            codigo = (String) tblFuncionario.getValueAt(linha,0);
            nome = (String) tblFuncionario.getValueAt(linha, 1 );
            rg = (String) tblFuncionario.getValueAt(linha, 2 );
            cpf = (String) tblFuncionario.getValueAt(linha, 3 );
            cpe = (String) tblFuncionario.getValueAt(linha, 4 );
            telefone = (String) tblFuncionario.getValueAt(linha, 5 );
//            setor = (String) tblFuncionario.getValueAt(linha, 6 );

            
            
            Alterar_Funcionarios alFuncionario = new Alterar_Funcionarios();
            alFuncionario.resgatar(codigo, nome, rg, cpf, cpe, telefone);
            alFuncionario.setVisible(true);
            this.dispose();

        }
    }//GEN-LAST:event_btnAlterar7ActionPerformed

